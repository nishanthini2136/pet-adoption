// Helper function to get a random image for a pet type
export const getRandomPetImage = (species, breed) => {
  const speciesImages = petImages[species.toLowerCase()];
  if (!speciesImages) return petImages.dogs.goldenRetriever.main;
  
  const breedImages = speciesImages[breed.toLowerCase().replace(/\s+/g, '')];
  if (!breedImages) return Object.values(speciesImages)[0].main;
  
  return breedImages.main;
};

// Helper function to get gallery images for a pet
export const getPetGallery = (species, breed) => {
  const speciesImages = petImages[species.toLowerCase()];
  if (!speciesImages) return petImages.dogs.goldenRetriever.gallery;
  
  const breedImages = speciesImages[breed.toLowerCase().replace(/\s+/g, '')];
  if (!breedImages) return Object.values(speciesImages)[0].gallery;
  
  return breedImages.gallery;
};

